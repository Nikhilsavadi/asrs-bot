def print_situational(results: list[DayResult]):
    """Breakdown by bar type, day of week, gap direction."""
    all_trades = [t for r in results for t in r.trades]
    if not all_trades:
        print("No trades to analyse.")
        return

    def _section(title, key_fn):
        groups = defaultdict(list)
        for t in all_trades:
            groups[key_fn(t)].append(t)

        print(f"\n-- {title} " + "-" * (60 - len(title)))
        print(f"{'Category':<15} {'Trades':>7} {'Win%':>6} {'AvgW':>7} {'AvgL':>7} {'Total':>8} {'Avg':>7}")
        print("-" * 65)
        for k in sorted(groups.keys()):
            trades = groups[k]
            wins = [t for t in trades if t.pnl_pts > 0]
            losses = [t for t in trades if t.pnl_pts < 0]
            total = sum(t.pnl_pts for t in trades)
            wr = round(len(wins) / len(trades) * 100) if trades else 0
            avg_w = round(np.mean([t.pnl_pts for t in wins]), 1) if wins else 0
            avg_l = round(np.mean([t.pnl_pts for t in losses]), 1) if losses else 0
            avg = round(total / len(trades), 1)
            print(f"{k:<15} {len(trades):>7} {wr:>5}% {avg_w:>+6.1f} {avg_l:>+6.1f} {total:>+7.1f} {avg:>+6.1f}")

    print(f"""
================================================================================
  FTSE SITUATIONAL ANALYSIS
  "When does the strategy work best?"
================================================================================""")

    _section("Bar Type", lambda t: t.bar_type)
    _section("Direction", lambda t: t.direction)
    _section("Day of Week", lambda t: t.day_of_week)
    _section("Gap Direction", lambda t: t.gap_dir or "N/A")
    _section("Stop Phase", lambda t: t.stop_phase)
    _section("Bar Width", lambda t: "NARROW(<15)" if t.bar_width < 15 else ("WIDE(>30)" if t.bar_width > 30 else "NORMAL"))
    _section("Prev Day", lambda t: t.prev_day_dir or "N/A")
    _section("Bar Type+Dir", lambda t: f"{t.bar_type}_{t.direction}")


def generate_charts(results: list[DayResult], label: str = "FTSE 1BN/1BP"):
    """Generate equity curve, drawdown, and distribution charts."""
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
        import matplotlib.dates as mdates
    except ImportError:
        logger.warning("matplotlib not available -- skipping charts")
        return

    all_trades = [t for r in results for t in r.trades]
    if not all_trades:
        return

    chart_dir = os.path.join(DATA_DIR, "charts")
    os.makedirs(chart_dir, exist_ok=True)

    # Equity curve
    dates = []
    equity = []
    running = 0
    for r in results:
        running += r.total_pnl
        if r.triggered:
            dates.append(datetime.strptime(r.date, "%Y-%m-%d"))
            equity.append(running)

    fig, axes = plt.subplots(2, 2, figsize=(16, 10))
    fig.suptitle(f"{label} Backtest Results", fontsize=16, fontweight="bold")

    # Equity curve
    ax = axes[0, 0]
    ax.plot(dates, equity, color="#2196F3", linewidth=1.5)
    ax.axhline(y=0, color="gray", linewidth=0.5, linestyle="--")
    ax.set_title("Equity Curve")
    ax.set_ylabel("Cumulative P&L (pts)")
    ax.grid(True, alpha=0.3)

    # Drawdown
    ax = axes[0, 1]
    peak = 0
    dd_vals = []
    for e in equity:
        peak = max(peak, e)
        dd_vals.append(peak - e)
    ax.fill_between(dates, dd_vals, color="#f44336", alpha=0.4)
    ax.set_title("Drawdown")
    ax.set_ylabel("Drawdown (pts)")
    ax.grid(True, alpha=0.3)

    # P&L distribution
    ax = axes[1, 0]
    pnls = [t.pnl_pts for t in all_trades]
    colors = ["#4CAF50" if p >= 0 else "#f44336" for p in pnls]
    ax.hist(pnls, bins=50, color="#2196F3", edgecolor="white", alpha=0.8)
    ax.axvline(x=0, color="gray", linewidth=1)
    ax.set_title("P&L Distribution")
    ax.set_xlabel("P&L (pts)")

    # Monthly P&L
    ax = axes[1, 1]
    monthly = defaultdict(float)
    for r in results:
        monthly[r.date[:7]] += r.total_pnl
    months = sorted(monthly.keys())
    vals = [monthly[m] for m in months]
    colors = ["#4CAF50" if v >= 0 else "#f44336" for v in vals]
    ax.bar(range(len(months)), vals, color=colors)
    ax.set_xticks(range(len(months)))
    ax.set_xticklabels(months, rotation=45, fontsize=7)
    ax.set_title("Monthly P&L")
    ax.set_ylabel("P&L (pts)")
    ax.grid(True, alpha=0.3, axis="y")

    plt.tight_layout()
    path = os.path.join(chart_dir, "ftse_backtest.png")
    fig.savefig(path, dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"\nChart saved to {path}")


def export_trades(results: list[DayResult], path: str = None):
    """Export all trades to CSV."""
    if path is None:
        path = os.path.join(DATA_DIR, "ftse_backtest_trades.csv")

    all_trades = [t for r in results for t in r.trades]
    if not all_trades:
        print("No trades to export.")
        return

    fields = [
        "date", "day_of_week", "bar_type", "direction",
        "entry_price", "exit_price", "pnl_pts", "mfe_pts", "mae_pts",
        "held_bars", "held_to_close", "stop_phase",
        "bar_width", "stake", "bar_open", "bar_high", "bar_low", "bar_close",
        "gap_dir", "gap_size", "prev_day_dir",
    ]
    with open(path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fields)
        writer.writeheader()
        for t in all_trades:
            writer.writerow({k: getattr(t, k, "") for k in fields})

    print(f"Exported {len(all_trades)} trades to {path}")


def compare_variants(df: pd.DataFrame):
    """Compare 3-phase stop vs simple candle trail."""
    print("\nRunning 3-phase stop backtest...")
    r1 = run_backtest(df)
    print("Running candle trail backtest...")
    r2 = run_backtest_candle_trail(df)

    t1 = [t for r in r1 for t in r.trades]
    t2 = [t for r in r2 for t in r.trades]

    def stats(trades):
        pnl = sum(t.pnl_pts for t in trades)
        wins = [t for t in trades if t.pnl_pts > 0]
        losses = [t for t in trades if t.pnl_pts < 0]
        wr = round(len(wins) / len(trades) * 100, 1) if trades else 0
        avg_w = round(np.mean([t.pnl_pts for t in wins]), 1) if wins else 0
        avg_l = round(np.mean([t.pnl_pts for t in losses]), 1) if losses else 0
        eq = []
        r = 0
        for t in trades:
            r += t.pnl_pts
            eq.append(r)
        pk = dd = 0
        for e in eq:
            pk = max(pk, e)
            dd = max(dd, pk - e)
        return {"n": len(trades), "pnl": pnl, "wr": wr, "avg_w": avg_w, "avg_l": avg_l, "dd": dd}

    s1 = stats(t1)
    s2 = stats(t2)

    print(f"""
================================================================================
  FTSE: 3-Phase Stop vs Candle Trail Comparison
================================================================================
                    {'3-Phase':>12}  {'Candle Trail':>12}
  Trades:           {s1['n']:>12}  {s2['n']:>12}
  Win rate:         {s1['wr']:>11}%  {s2['wr']:>11}%
  Avg win:          {s1['avg_w']:>+11.1f}  {s2['avg_w']:>+11.1f}
  Avg loss:         {s1['avg_l']:>+11.1f}  {s2['avg_l']:>+11.1f}
  Total P&L:        {s1['pnl']:>+11.1f}  {s2['pnl']:>+11.1f}
  Max DD:           {s1['dd']:>11.1f}  {s2['dd']:>11.1f}
================================================================================
""")


