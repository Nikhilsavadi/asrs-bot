"""
backtest.py -- FTSE 1BN/1BP Backtester
==============================================================================

Runs Tom Hougaard's First Bar Negative / First Bar Positive strategy
over historical 5-min FTSE data.

Usage:
    python -m ftse_bot.backtest                    Full backtest + summary
    python -m ftse_bot.backtest --situational      Breakdown by bar type, day, etc.
    python -m ftse_bot.backtest --export           Export trades to CSV
    python -m ftse_bot.backtest --charts           Generate equity/drawdown charts
    python -m ftse_bot.backtest --month 2025-06    Filter to specific month

Data:
    Reads from data/ftse/ftse_rth.parquet (RTH bars)
    and data/ftse/ftse_all.parquet (all-hours, for pre-market bar)
    Generated by fetch_ftse.py
"""

