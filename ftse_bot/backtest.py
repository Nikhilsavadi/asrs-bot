@dataclass
class Trade:
    date:          str = ""
    day_of_week:   str = ""
    bar_type:      str = ""        # 1BN, 1BP
    direction:     str = ""        # LONG, SHORT
    entry_price:   float = 0.0
    exit_price:    float = 0.0
    pnl_pts:       float = 0.0
    mfe_pts:       float = 0.0
    mae_pts:       float = 0.0
    held_bars:     int = 0
    held_to_close: bool = False

    # Stop management
    stop_phase:    str = ""        # INITIAL, BREAKEVEN, TRAILING
    bar_width:     float = 0.0
    stake:         float = 1.0

    # Bar data
    bar_open:      float = 0.0
    bar_high:      float = 0.0
    bar_low:       float = 0.0
    bar_close:     float = 0.0

    # Situational
    prev_day_dir:  str = ""        # UP / DOWN
    gap_dir:       str = ""        # GAP_UP / GAP_DOWN / FLAT
    gap_size:      float = 0.0


@dataclass
class DayResult:
    date:          str = ""
    trade_date:    date = None
    day_of_week:   str = ""
    bar_type:      str = ""
    bar_width:     float = 0.0
    bar_open:      float = 0.0
    bar_high:      float = 0.0
    bar_low:       float = 0.0
    bar_close:     float = 0.0
    buy_level:     float = 0.0
    sell_level:    float = 0.0
    stake:         float = 1.0
    gap_dir:       str = ""
    gap_size:      float = 0.0
    trades:        list = field(default_factory=list)
    total_pnl:     float = 0.0
    triggered:     bool = False
    skipped:       bool = False      # Doji skip


# ==============================================================================
#  STRATEGY HELPERS
# ==============================================================================

BUFFER_PTS = 1.0
BAR_WIDTH_THRESHOLD = 30.0
BREAKEVEN_TRIGGER = 10.0
STAKE_PER_POINT = 1.0


def classify_bar(o: float, h: float, l: float, c: float) -> str:
    """Classify the first 5-min bar."""
    if c < o:
        return "1BN"
    elif c > o:
        return "1BP"
    return "DOJI"


def classify_gap(prev_close: float, today_open: float) -> tuple[str, float]:
    if prev_close == 0:
        return "FLAT", 0
    gap = round(today_open - prev_close, 1)
    if gap > 10:
        return "GAP_UP", gap
    elif gap < -10:
        return "GAP_DOWN", gap
    return "FLAT", gap


# ==============================================================================
#  BACKTEST ENGINE
# ==============================================================================

def run_backtest(
    df: pd.DataFrame,
    doji_action: str = "SKIP",
    buffer_pts: float = BUFFER_PTS,
    bar_width_threshold: float = BAR_WIDTH_THRESHOLD,
    breakeven_trigger: float = BREAKEVEN_TRIGGER,
    stake_per_point: float = STAKE_PER_POINT,
) -> list[DayResult]:
    """
    Run 1BN/1BP backtest over all trading days.

    For each day:
      1. Find the 08:00-08:05 bar
      2. Classify as 1BN / 1BP / DOJI
      3. Determine entry levels + stop
      4. Simulate fills on subsequent bars
      5. Manage stop: INITIAL -> BREAKEVEN -> TRAILING
      6. Hard close at 16:30 if still open
    """
    results = []
    prev_close = 0.0

    # Group by trading day
    trading_days = sorted(set(df.index.date))
    logger.info(f"Backtesting {len(trading_days)} trading days...")

    for trade_date in trading_days:
        day_df = df[df.index.date == trade_date]
        if len(day_df) < 5:
            prev_close = day_df.iloc[-1]["Close"] if not day_df.empty else prev_close
            continue

        day_name = trade_date.strftime("%A")

        # Skip weekends (shouldn't be in data, but just in case)
        if trade_date.weekday() >= 5:
            continue

        # Find the 08:00 bar (covers 08:00-08:05)
        first_bar = None
        for idx, row in day_df.iterrows():
            if idx.hour == 8 and idx.minute == 0:
                first_bar = row
                break
            # Some feeds label by bar close time
            if idx.hour == 8 and idx.minute == 5:
                first_bar = row
                break
            # Some feeds use 07:55 for the bar ending at 08:00
            # Try the first bar at or after 08:00
            if idx.hour >= 8 and first_bar is None:
                first_bar = row
                break

        if first_bar is None:
            prev_close = day_df.iloc[-1]["Close"] if not day_df.empty else prev_close
            continue

        o = round(first_bar["Open"], 1)
        h = round(first_bar["High"], 1)
        l = round(first_bar["Low"], 1)
        c = round(first_bar["Close"], 1)
        width = round(h - l, 1)

        bar_type = classify_bar(o, h, l, c)

        # Gap
        gap_dir, gap_size = classify_gap(prev_close, o) if prev_close > 0 else ("FLAT", 0)

        # Previous day direction
        prev_day_dir = ""
        if prev_close > 0 and not day_df.empty:
            prev_day_dir = "UP" if day_df.iloc[0]["Open"] > prev_close else "DOWN"

        # Stake sizing
        stake = stake_per_point
        if width > bar_width_threshold:
            stake = round(stake_per_point / 2, 2)

        # Setup day result
        day_result = DayResult(
            date=str(trade_date), trade_date=trade_date,
            day_of_week=day_name, bar_type=bar_type,
            bar_width=width, bar_open=o, bar_high=h, bar_low=l, bar_close=c,
            stake=stake, gap_dir=gap_dir, gap_size=gap_size,
        )

        # Handle doji
        resolved_type = bar_type
        if bar_type == "DOJI":
            if doji_action == "SKIP":
                day_result.skipped = True
                results.append(day_result)
                prev_close = day_df.iloc[-1]["Close"]
                continue
            elif doji_action == "TREAT_AS_1BN":
                resolved_type = "1BN"
            elif doji_action == "TREAT_AS_1BP":
                resolved_type = "1BP"

        # Entry levels
        buy_level = round(l - buffer_pts, 1)   # Buy stop below bar low
        sell_level = round(h + buffer_pts, 1)   # Sell stop above bar high

        day_result.buy_level = buy_level
        day_result.sell_level = sell_level

        # Determine which orders to place
        # 1BN: buy stop below AND sell stop above (OCO)
        # 1BP: sell stop below only
        if resolved_type == "1BN":
            place_buy = True
            place_sell = True
        else:  # 1BP
            place_buy = False
            place_sell = True
            # 1BP: sell stop is below the bar low
            sell_level = buy_level  # sell below bar low

        # Get post-bar candles (08:05 onwards until 16:30)
        first_bar_idx = day_df.index.get_loc(first_bar.name)
        if isinstance(first_bar_idx, slice):
            first_bar_idx = first_bar_idx.start
        post_bars = list(day_df.iloc[first_bar_idx + 1:].iterrows())

        # Filter to session (up to 16:30)
        session_bars = []
        for idx, row in post_bars:
            if idx.hour > 16 or (idx.hour == 16 and idx.minute > 25):
                break
            session_bars.append((idx, row))

        # Simulate order execution
        direction = None
        entry_price = 0.0
        initial_stop = 0.0
        trail_stop = 0.0
        stop_phase = "INITIAL"
        mfe = 0.0
        mae = 0.0
        entry_bar_idx = 0
        max_price = 0.0  # For trailing

        for i, (idx, row) in enumerate(session_bars):
            # -- Check for entry --
            if direction is None:
                triggered = False

                if place_buy and row["High"] >= buy_level and place_sell and row["Low"] <= sell_level:
                    # Both could trigger on same bar -- use open direction
                    if abs(row["Open"] - buy_level) < abs(row["Open"] - sell_level):
                        direction = "LONG"
                        entry_price = buy_level
                    else:
                        direction = "SHORT"
                        entry_price = sell_level
                    triggered = True
                elif place_buy and row["High"] >= buy_level:
                    direction = "LONG"
                    entry_price = buy_level
                    triggered = True
                elif place_sell and row["Low"] <= sell_level:
                    direction = "SHORT"
                    entry_price = sell_level
                    triggered = True

                if triggered:
                    if direction == "LONG":
                        initial_stop = round(entry_price - width, 1)
                    else:
                        initial_stop = round(entry_price + width, 1)
                    trail_stop = initial_stop
                    stop_phase = "INITIAL"
                    mfe = 0.0
                    mae = 0.0
                    max_price = entry_price
                    entry_bar_idx = i
                    continue

            # -- Manage position --
            if direction == "LONG":
                bar_mfe = row["High"] - entry_price
                bar_mae = entry_price - row["Low"]
                mfe = max(mfe, bar_mfe)
                mae = max(mae, bar_mae)
                max_price = max(max_price, row["High"])

                profit = row["High"] - entry_price

                # Phase transitions
                if stop_phase == "INITIAL" and profit >= breakeven_trigger:
                    stop_phase = "BREAKEVEN"
                    trail_stop = entry_price

                if stop_phase == "BREAKEVEN":
                    # Start trailing
                    new_stop = round(max_price - width, 1)
                    if new_stop > trail_stop:
                        trail_stop = new_stop
                        stop_phase = "TRAILING"

                if stop_phase == "TRAILING":
                    new_stop = round(max_price - width, 1)
                    if new_stop > trail_stop:
                        trail_stop = new_stop

                # Stop check
                if row["Low"] <= trail_stop:
                    pnl = round(trail_stop - entry_price, 1)
                    trade = Trade(
                        date=str(trade_date), day_of_week=day_name,
                        bar_type=resolved_type, direction="LONG",
                        entry_price=entry_price, exit_price=trail_stop,
                        pnl_pts=pnl, mfe_pts=round(mfe, 1), mae_pts=round(mae, 1),
                        held_bars=i - entry_bar_idx, stop_phase=stop_phase,
                        bar_width=width, stake=stake,
                        bar_open=o, bar_high=h, bar_low=l, bar_close=c,
                        prev_day_dir=prev_day_dir, gap_dir=gap_dir, gap_size=gap_size,
                    )
                    day_result.trades.append(trade)
                    direction = None  # Only 1 trade per day

            elif direction == "SHORT":
                bar_mfe = entry_price - row["Low"]
                bar_mae = row["High"] - entry_price
                mfe = max(mfe, bar_mfe)
                mae = max(mae, bar_mae)
                max_price = min(max_price if max_price < entry_price else row["Low"], row["Low"])

                profit = entry_price - row["Low"]

                if stop_phase == "INITIAL" and profit >= breakeven_trigger:
                    stop_phase = "BREAKEVEN"
                    trail_stop = entry_price

                if stop_phase == "BREAKEVEN":
                    new_stop = round(max_price + width, 1)
                    if new_stop < trail_stop:
                        trail_stop = new_stop
                        stop_phase = "TRAILING"

                if stop_phase == "TRAILING":
                    new_stop = round(max_price + width, 1)
                    if new_stop < trail_stop:
                        trail_stop = new_stop

                if row["High"] >= trail_stop:
                    pnl = round(entry_price - trail_stop, 1)
                    trade = Trade(
                        date=str(trade_date), day_of_week=day_name,
                        bar_type=resolved_type, direction="SHORT",
                        entry_price=entry_price, exit_price=trail_stop,
                        pnl_pts=pnl, mfe_pts=round(mfe, 1), mae_pts=round(mae, 1),
                        held_bars=i - entry_bar_idx, stop_phase=stop_phase,
                        bar_width=width, stake=stake,
                        bar_open=o, bar_high=h, bar_low=l, bar_close=c,
                        prev_day_dir=prev_day_dir, gap_dir=gap_dir, gap_size=gap_size,
                    )
                    day_result.trades.append(trade)
                    direction = None

        # Hard close at 16:30 if still holding
        if direction is not None and session_bars:
            last_price = round(session_bars[-1][1]["Close"], 1)
            if direction == "LONG":
                pnl = round(last_price - entry_price, 1)
            else:
                pnl = round(entry_price - last_price, 1)

            trade = Trade(
                date=str(trade_date), day_of_week=day_name,
                bar_type=resolved_type, direction=direction,
                entry_price=entry_price, exit_price=last_price,
                pnl_pts=pnl, mfe_pts=round(mfe, 1), mae_pts=round(mae, 1),
                held_bars=len(session_bars) - entry_bar_idx, held_to_close=True,
                stop_phase=stop_phase, bar_width=width, stake=stake,
                bar_open=o, bar_high=h, bar_low=l, bar_close=c,
                prev_day_dir=prev_day_dir, gap_dir=gap_dir, gap_size=gap_size,
            )
            day_result.trades.append(trade)

        day_result.total_pnl = round(sum(t.pnl_pts for t in day_result.trades), 1)
        day_result.triggered = len(day_result.trades) > 0
        results.append(day_result)
        prev_close = day_df.iloc[-1]["Close"]

    return results


