def load_data() -> tuple[pd.DataFrame | None, pd.DataFrame | None]:
    """Load cached FTSE data from parquet files."""
    rth_path = os.path.join(DATA_DIR, "ftse_rth.parquet")
    all_path = os.path.join(DATA_DIR, "ftse_all.parquet")

    rth_df = None
    all_df = None

    if os.path.exists(all_path):
        all_df = pd.read_parquet(all_path)
        if all_df.index.tz is None:
            all_df.index = all_df.index.tz_localize("Europe/London")
        logger.info(f"Loaded all-hours data: {len(all_df)} bars")

    if os.path.exists(rth_path):
        rth_df = pd.read_parquet(rth_path)
        if rth_df.index.tz is None:
            rth_df.index = rth_df.index.tz_localize("Europe/London")
        logger.info(f"Loaded RTH data: {len(rth_df)} bars")

    # If we only have all-hours, use that for everything
    if rth_df is None and all_df is not None:
        rth_df = all_df
        logger.info("Using all-hours data as main dataset")

    return rth_df, all_df


