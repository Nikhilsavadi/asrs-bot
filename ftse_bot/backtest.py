def main():
    args = sys.argv[1:] if len(sys.argv) > 1 else []

    df, all_df = load_data()
    if df is None:
        print("No FTSE data found. Run fetch_ftse.py first to download from IBKR.")
        print(f"Expected at: {os.path.join(DATA_DIR, 'ftse_rth.parquet')}")
        print(f"         or: {os.path.join(DATA_DIR, 'ftse_all.parquet')}")
        return

    # Use all_df if available (includes pre-market 08:00 bar)
    data = all_df if all_df is not None else df

    print(f"Data: {len(data)} bars, {data.index[0]} -> {data.index[-1]}")

    # Month filter
    month_filter = ""
    if "--month" in args:
        idx = args.index("--month")
        if idx + 1 < len(args):
            month_filter = args[idx + 1]

    if "--compare" in args:
        compare_variants(data)
        return

    # Run default backtest
    results = run_backtest(data)

    if month_filter:
        results = [r for r in results if r.date.startswith(month_filter)]

    if "--situational" in args:
        print_summary(results)
        print_situational(results)
    elif "--export" in args:
        print_summary(results)
        export_trades(results)
    elif "--charts" in args:
        print_summary(results)
        generate_charts(results)
    else:
        print_summary(results)

    if "--charts" in args or len(args) == 0:
        try:
            generate_charts(results)
        except Exception as e:
            logger.warning(f"Chart generation failed: {e}")


if __name__ == "__main__":
    main()
