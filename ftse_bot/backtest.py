def print_summary(results: list[DayResult], label: str = "FTSE 1BN/1BP"):
    """Print overall backtest summary."""
    all_trades = [t for r in results for t in r.trades]
    triggered_days = [r for r in results if r.triggered]
    skipped_days = [r for r in results if r.skipped]
    win_trades = [t for t in all_trades if t.pnl_pts > 0]
    lose_trades = [t for t in all_trades if t.pnl_pts < 0]

    if not all_trades:
        print("No trades generated.")
        return

    total_pnl = sum(t.pnl_pts for t in all_trades)
    total_gbp = sum(t.pnl_pts * t.stake for t in all_trades)
    win_days = [r for r in results if r.total_pnl > 0]
    lose_days = [r for r in results if r.total_pnl < 0]
    held_to_close = [t for t in all_trades if t.held_to_close]

    # Bar type breakdown
    bn_trades = [t for t in all_trades if t.bar_type == "1BN"]
    bp_trades = [t for t in all_trades if t.bar_type == "1BP"]

    # Streaks
    max_win_streak = max_lose_streak = 0
    cur_win = cur_lose = 0
    for r in triggered_days:
        if r.total_pnl > 0:
            cur_win += 1; cur_lose = 0
            max_win_streak = max(max_win_streak, cur_win)
        elif r.total_pnl < 0:
            cur_lose += 1; cur_win = 0
            max_lose_streak = max(max_lose_streak, cur_lose)
        else:
            cur_win = cur_lose = 0

    # Drawdown
    equity = []
    running = 0
    peak = max_dd = 0
    for r in results:
        running += r.total_pnl
        equity.append(running)
        peak = max(peak, running)
