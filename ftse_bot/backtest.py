def run_backtest_candle_trail(
    df: pd.DataFrame,
    doji_action: str = "SKIP",
    buffer_pts: float = BUFFER_PTS,
) -> list[DayResult]:
    """
    Alternative: trail using previous candle low/high instead of
    the 3-phase breakeven/trailing system.
    """
    results = []
    prev_close = 0.0
    trading_days = sorted(set(df.index.date))

    for trade_date in trading_days:
        day_df = df[df.index.date == trade_date]
        if len(day_df) < 5 or trade_date.weekday() >= 5:
            prev_close = day_df.iloc[-1]["Close"] if not day_df.empty else prev_close
            continue

        day_name = trade_date.strftime("%A")

        # Find 08:00 bar
        first_bar = None
        for idx, row in day_df.iterrows():
            if idx.hour == 8 and idx.minute in (0, 5):
                first_bar = row
                break
            if idx.hour >= 8 and first_bar is None:
                first_bar = row
                break

        if first_bar is None:
            prev_close = day_df.iloc[-1]["Close"]
            continue

        o, h, l, c = (round(first_bar["Open"], 1), round(first_bar["High"], 1),
                       round(first_bar["Low"], 1), round(first_bar["Close"], 1))
        width = round(h - l, 1)
        bar_type = classify_bar(o, h, l, c)

        gap_dir, gap_size = classify_gap(prev_close, o) if prev_close > 0 else ("FLAT", 0)

        day_result = DayResult(
            date=str(trade_date), trade_date=trade_date,
            day_of_week=day_name, bar_type=bar_type,
            bar_width=width, bar_open=o, bar_high=h, bar_low=l, bar_close=c,
            gap_dir=gap_dir, gap_size=gap_size,
        )

        resolved_type = bar_type
        if bar_type == "DOJI":
            if doji_action == "SKIP":
                day_result.skipped = True
                results.append(day_result)
                prev_close = day_df.iloc[-1]["Close"]
                continue
            elif doji_action == "TREAT_AS_1BN":
                resolved_type = "1BN"
            elif doji_action == "TREAT_AS_1BP":
                resolved_type = "1BP"

        buy_level = round(l - buffer_pts, 1)
        sell_level = round(h + buffer_pts, 1)
        day_result.buy_level = buy_level
        day_result.sell_level = sell_level

        if resolved_type == "1BN":
            place_buy, place_sell = True, True
        else:
            place_buy, place_sell = False, True
            sell_level = buy_level

        first_bar_idx_loc = day_df.index.get_loc(first_bar.name)
        if isinstance(first_bar_idx_loc, slice):
            first_bar_idx_loc = first_bar_idx_loc.start
        post_bars = list(day_df.iloc[first_bar_idx_loc + 1:].iterrows())

        session_bars = []
        for idx, row in post_bars:
            if idx.hour > 16 or (idx.hour == 16 and idx.minute > 25):
                break
            session_bars.append((idx, row))

        direction = None
        entry_price = 0.0
        trail_stop = 0.0
        mfe = mae = 0.0
        entry_bar_idx = 0

        for i, (idx, row) in enumerate(session_bars):
            if direction is None:
                triggered = False
                if place_buy and row["High"] >= buy_level and place_sell and row["Low"] <= sell_level:
                    if abs(row["Open"] - buy_level) < abs(row["Open"] - sell_level):
                        direction, entry_price = "LONG", buy_level
                    else:
                        direction, entry_price = "SHORT", sell_level
                    triggered = True
                elif place_buy and row["High"] >= buy_level:
                    direction, entry_price = "LONG", buy_level
                    triggered = True
                elif place_sell and row["Low"] <= sell_level:
                    direction, entry_price = "SHORT", sell_level
                    triggered = True

                if triggered:
                    trail_stop = round(l - buffer_pts, 1) if direction == "LONG" else round(h + buffer_pts, 1)
                    mfe = mae = 0.0
                    entry_bar_idx = i
                    continue

            if direction == "LONG":
                mfe = max(mfe, row["High"] - entry_price)
                mae = max(mae, entry_price - row["Low"])

                # Trail to previous candle low
                if i > entry_bar_idx:
                    prev_low = round(session_bars[i - 1][1]["Low"], 1)
                    if prev_low > trail_stop:
                        trail_stop = prev_low

                if row["Low"] <= trail_stop:
                    pnl = round(trail_stop - entry_price, 1)
                    day_result.trades.append(Trade(
                        date=str(trade_date), day_of_week=day_name,
                        bar_type=resolved_type, direction="LONG",
                        entry_price=entry_price, exit_price=trail_stop,
                        pnl_pts=pnl, mfe_pts=round(mfe, 1), mae_pts=round(mae, 1),
                        held_bars=i - entry_bar_idx, stop_phase="CANDLE_TRAIL",
                        bar_width=width, bar_open=o, bar_high=h, bar_low=l, bar_close=c,
                        gap_dir=gap_dir, gap_size=gap_size,
                    ))
                    direction = None

            elif direction == "SHORT":
                mfe = max(mfe, entry_price - row["Low"])
                mae = max(mae, row["High"] - entry_price)

                if i > entry_bar_idx:
                    prev_high = round(session_bars[i - 1][1]["High"], 1)
                    if prev_high < trail_stop:
                        trail_stop = prev_high

                if row["High"] >= trail_stop:
                    pnl = round(entry_price - trail_stop, 1)
                    day_result.trades.append(Trade(
                        date=str(trade_date), day_of_week=day_name,
                        bar_type=resolved_type, direction="SHORT",
                        entry_price=entry_price, exit_price=trail_stop,
                        pnl_pts=pnl, mfe_pts=round(mfe, 1), mae_pts=round(mae, 1),
                        held_bars=i - entry_bar_idx, stop_phase="CANDLE_TRAIL",
                        bar_width=width, bar_open=o, bar_high=h, bar_low=l, bar_close=c,
                        gap_dir=gap_dir, gap_size=gap_size,
                    ))
                    direction = None

        # Hard close at 16:30
        if direction is not None and session_bars:
            last_price = round(session_bars[-1][1]["Close"], 1)
            pnl = round((last_price - entry_price) if direction == "LONG" else (entry_price - last_price), 1)
            day_result.trades.append(Trade(
                date=str(trade_date), day_of_week=day_name,
                bar_type=resolved_type, direction=direction,
                entry_price=entry_price, exit_price=last_price,
                pnl_pts=pnl, mfe_pts=round(mfe, 1), mae_pts=round(mae, 1),
                held_bars=len(session_bars) - entry_bar_idx, held_to_close=True,
                stop_phase="SESSION_CLOSE", bar_width=width,
                bar_open=o, bar_high=h, bar_low=l, bar_close=c,
                gap_dir=gap_dir, gap_size=gap_size,
            ))

        day_result.total_pnl = round(sum(t.pnl_pts for t in day_result.trades), 1)
        day_result.triggered = len(day_result.trades) > 0
        results.append(day_result)
        prev_close = day_df.iloc[-1]["Close"]

    return results


